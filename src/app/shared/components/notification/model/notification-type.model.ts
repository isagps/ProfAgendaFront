/**
 * Tipo que representa os diferentes tipos de notificações disponíveis.
 * 
 * @typedef {string} NotificationType
 * 
 * @property {'success'} - Representa uma notificação de sucesso.
 * @property {'error'} - Representa uma notificação de erro.
 * @property {'info'} - Representa uma notificação informativa.
 */
export type NotificationType = 'success' | 'error' | 'info';